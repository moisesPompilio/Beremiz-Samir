package com.samirAtivacao.modelo;

public class InformacoesCessado {

    private String especie;
    private String dataDeinicio;
    private String dataFinalBeneficio;
    private String rmi;

    private String nb;



    public String getEspecie() {
        return especie;
    }

    public void setEspecie(String especie) {
        this.especie = especie;
    }

    public String getDataDeinicio() {
        return dataDeinicio;
    }

    public void setDataDeinicio(String dataDeinicio) {
        this.dataDeinicio = dataDeinicio;
    }

    public String getDataFinalBeneficio() {
        return dataFinalBeneficio;
    }

    public void setDataFinalBeneficio(String dataFinalBeneficio) {
        this.dataFinalBeneficio = dataFinalBeneficio;
    }

    public String getRmi() {
        return rmi;
    }

    public void setRmi(String rmi) {
        this.rmi = rmi;
    }

    public String getNb() {
        return nb;
    }

    public void setNb(String nb) {
        this.nb = nb;
    }
}
